    public void getData(){
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
                    
        try {
            Statement stat = (Statement) conek.GetConnection().createStatement();
            String sql = "Select * from tabkost1";
            ResultSet res = stat.executeQuery (sql);
            
        while(res.next()){
            Object[]obj = new Object[6];
            obj[0] = res.getString("Registrasi");
            obj[1] = res.getString("Nama");
            obj[2] = res.getString("Gender");
            obj[3] = res.getString("Agama");
            obj[4] = res.getString("Pekerjaan");
            obj[5] = res.getString("Telepon");
            
            model.addRow(obj);
                }
        }catch(SQLException err){
            JOptionPane.showMessageDialog(null, err.getMessage());
            }
}
    
