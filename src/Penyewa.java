    private void BSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BSaveActionPerformed
        // TODO add your handling code here:
        String registrasi = (String) CBRegis.getSelectedItem();
        String nama = TNama.getText();
        String agama = (String) CBAgama.getSelectedItem();
        String pekerjaan = TPekerjaan.getText();
        String telepon = THp.getText();
        String gender = null;
        if (jRadioButton1.isSelected()){
            gender = "Laki-laki";
        }
        else
        if (jRadioButton2.isSelected()){
            gender = "Perempuan";
        }    
        
        try {
            Statement statement = (Statement)conek.GetConnection().createStatement();
            statement.executeUpdate("insert into tabkost1 VALUES('"+ registrasi +"','"+ nama +"','"+ agama +"','"+ gender +"','"+ pekerjaan +"','"+ telepon +"');");
            statement.close();
            JOptionPane.showMessageDialog(null,"Data berhasil disimpan!");
            bersih();
        }
        catch (Exception t){
            JOptionPane.showMessageDialog(null,"Data gagal disimpan!");
        }
        getData();
    }//GEN-LAST:event_BSaveActionPerformed

